# 도메인 디렉터리
# 파이포 프로젝트는 질문과 답변을 작성하는 게시판을 만드는 것을 최종 목표로 하고 있다.
# 질문/답변/사용자 라는 총 3개의 도메인을 두어 그 하위에 관련된 파일 작성.
# 그리고 각 도메인은 API를 생성하기 위해서 다음과 같은 파일들이 필요함. 
# 1. 라이터 파일 - URL과 API의 전체적인 동작을 관리
# 2. 데이터베이스 처리 파일 - 데이터의 생성(Create), 조회(Read), 수정(Update), 삭제(Delete)를 처리 [CRUD]
# 3. 입출력 관리 파일 - 입력 데이터와 출력 데이터의 스펙 정의 및 검증

